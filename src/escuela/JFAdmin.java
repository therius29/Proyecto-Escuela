        jTextField1.setText(String.valueOf(jTable1.getValueAt(seleccionado, 0)));
        jTextField2.setText(String.valueOf(jTable1.getValueAt(seleccionado, 1)));
        jTextField3.setText(String.valueOf(jTable1.getValueAt(seleccionado, 2)));
        jTextField4.setText(String.valueOf(jTable1.getValueAt(seleccionado, 3)));
        jTextField5.setText(String.valueOf(jTable1.getValueAt(seleccionado, 4)));
        //capturando el email a actualizar
        emailAnterior=String.valueOf(jTable1.getValueAt(seleccionado, 4));
